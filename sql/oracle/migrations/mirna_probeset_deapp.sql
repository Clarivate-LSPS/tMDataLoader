--
  CREATE OR REPLACE TRIGGER "TM_DATALOADER"."TRG_MIRNA_PROBESET_DEAPP" 
    before insert on "TM_DATALOADER"."MIRNA_PROBESET_DEAPP"    
	for each row begin     
		if inserting then       
			if :NEW."PROBESET_ID" is null then
